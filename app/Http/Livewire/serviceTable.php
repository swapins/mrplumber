    |
    | ❗ IMPORTANT: When using closures, you must escape any value coming from
    |    the database using the `e()` Laravel Helper function.
    |
    */
    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('serviceName')

           /** Example of custom column using a closure **/
            ->addColumn('serviceName_lower', function (services $model) {
                return strtolower(e($model->serviceName));
            })

            ->addColumn('iconName')
            ->addColumn('dec')
            ->addColumn('enable')
            ->addColumn('imagePath')
            ->addColumn('created_at_formatted', fn (services $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'))
            ->addColumn('updated_at_formatted', fn (services $model) => Carbon::parse($model->updated_at)->format('d/m/Y H:i:s'));
    }

    /*
